# ╔═╡ 04df3692-cc67-4e92-afea-744e885ec7b3
threshold_cb = let
	θ = 1. # Threshold
	function condition(u, t, integrator)
		(u[1] - θ)
	end
	P = 4. # time width of plateau
	function affect!(integrator)
		integrator.u[2] = P
	end
	ContinuousCallback(condition, affect!, nothing)
