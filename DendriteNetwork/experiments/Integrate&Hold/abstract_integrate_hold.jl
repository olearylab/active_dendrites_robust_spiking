# ╔═╡ 4586bd29-2e44-49a0-8b6f-4cc7d281e731
function synapse_with_input(input_times; name, τ_s)
	@named synapse = Synapse(;τ_s = τ_s)
	@named EPSPs = input(input_times)
	connection = ODESystem([synapse.x ~ 50*EPSPs.x], t, name=:connection)
	compose(connection, synapse, EPSPs; name)
end

# ╔═╡ 4c45d969-c65b-4e62-9840-4e735edf58ef
@named synapse1 = synapse_with_input([5.]; τ_s = 10.)

# ╔═╡ 1ff92bea-5d46-4cef-bcc8-c6bef911d65c
@named synapse2 = synapse_with_input([15.]; τ_s = 10.)

# ╔═╡ d96767b3-9630-4610-a2da-08194d7ac1f0
@named synapse3 = synapse_with_input([25.]; τ_s = 10.)

# ╔═╡ 89a4d189-25f8-45a6-984e-13a703c3fa2a
# @named connections = ODESystem([
# 	soma.I ~ (synapse1.synapse.V - soma.V) + (synapse2.synapse.V - soma.V) +
# 	(synapse3.synapse.V - soma.V)
# ], t)
@named connections = ODESystem([
	soma.I ~ (synapse1.synapse.V - soma.V) + (synapse2.synapse.V - soma.V) +
	1.2*(synapse3.synapse.V - soma.V)
], t)

# ╔═╡ d747d755-c8e5-41d9-bed5-f40667bd1e25
soma_with_synapses = structural_simplify(compose(connections, synapse1, synapse2, synapse3, soma))

# ╔═╡ f8dfa14d-f4fd-4f29-9983-8bc5462f49f2
soma_synapses_prob = ODEProblem(soma_with_synapses, [], (0., 100.), [])

# ╔═╡ 5e0e986c-5046-40a7-acfb-1a06184a9268
plateau_cb2 = let
	θ = 1.5 # Plateau threshold
	callbacks = []
	for V in [synapse1.synapse.V, synapse2.synapse.V]
		index = indexof(V, states(soma_with_synapses))
		condition(u, t, integrator) = integrator.u[index] > θ
		affect!(integrator) = integrator.u[index] = θ
		push!(callbacks, DiscreteCallback(condition, affect!))
	end
	CallbackSet(callbacks...)
end

# ╔═╡ aee58af6-54ac-4182-9654-2065d1303333
threshold_cb2 = let
	θ = 1.5 # Plateau threshold
	P = 20. # Timewidth of plateau
	callbacks = []
	for (V, T) in [(synapse1.synapse.V, synapse1.synapse.T), (synapse2.synapse.V, synapse2.synapse.T), (synapse3.synapse.V, synapse3.synapse.T)]
		V_index = indexof(V, states(soma_with_synapses))
		T_index = indexof(T, states(soma_with_synapses))
		function condition(u, t, integrator)
			(u[V_index] - θ)
		end
		function affect!(integrator)
			integrator.u[T_index] = P
		end
		push!(callbacks, ContinuousCallback(condition, affect!, nothing))
	end
	CallbackSet(callbacks...)
end

# ╔═╡ 0f8a7635-360d-46fa-85bc-3449aa191e78
spiketimes = Float32[]

# ╔═╡ cb1bf2d7-70d0-4640-879f-e39359db5bb6
begin
	spiking_cb2 = let
		θ = 1. # Spiking threshold
		callbacks = []
		for (V, g_r) in [(soma.V, soma.g_r)]
			V_index = indexof(V, states(soma_with_synapses))
			g_r_index = indexof(g_r, states(soma_with_synapses))
			
			condition(u, t, integrator) = u[V_index] - θ
			function affect!(integrator) 
				integrator.u[V_index] = θ
				integrator.u[g_r_index] = 5.
				push!(spiketimes, integrator.t)
			end
			push!(callbacks, ContinuousCallback(condition, affect!))
		end
		CallbackSet(callbacks...)
	end
end

# ╔═╡ aee13b2c-a23a-41a7-bfa9-cac446257cf7
soma_with_synapses_sol = solve(soma_synapses_prob, Rodas5(); 
	callback = CallbackSet(plateau_cb2, threshold_cb2, spiking_cb2), 
	reltol = 1e-4, abstol = 1e-4);

# ╔═╡ 304be071-d33d-4138-bbad-bde3cbe0a32c
soma_without_synapses_sol = solve(soma_synapses_prob, Rodas5();
