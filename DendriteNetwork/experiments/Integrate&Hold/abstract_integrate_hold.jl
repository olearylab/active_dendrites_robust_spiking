# ╔═╡ 1513a60a-7088-4e31-9401-d851f9d5a48c
function Soma(;name, τ_m = 1.0, R = 1.0, θ = 1.0)
	# Specifying the dynamics:
	states = @variables V(t)=0. I(t)=0. g_r(t)=0. # Voltage, current
	params = @parameters τ_m=τ_m R=R # Membrane time constant
	eqs = [
		D(V) ~ (-V + R*I - g_r*V) / τ_m,
		D(g_r) ~ -g_r / (2*τ_m)
	]
	
	ODESystem(eqs, t, states, params; name=name)
end

# ╔═╡ e0150aa3-a04f-430b-aa96-f5664b1567d0
function Synapse(;name, τ_s = 1.0, R = 1.0)
	states = @variables V(t)=0. T(t)=0. x(t)
	params = @parameters τ_s=τ_s R=R
	eqs = [
		D(V) ~ IfElse.ifelse(T < 0, (-V + R*x) / τ_s, 1e-3),
		D(T) ~ -step(T, 0)
	]
	ODESystem(eqs, t, states, params; name=name)
end

