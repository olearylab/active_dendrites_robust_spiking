function gammapdf(t)
	k = 2.
	σ0 = 1.
	v0 = σ0^2
	θ = sqrt(v0)
	pdf(Gamma(k, θ), t)
end

# ╔═╡ 433b5b83-70a4-47c6-a800-79995a26ed0b
